#!/bin/bash

# Активируем виртуальное окружение
source venv/bin/activate

# Запускаем скрипт очистки данных
echo "Запуск очистки данных..."
python clear_data.py

# Небольшая пауза для завершения операций
sleep 2

# Запускаем скрипт генерации тестовых данных
echo "Запуск генерации тестовых данных..."
python generate_test_data.py

# Деактивируем виртуальное окружение
deactivate

echo "Операция завершена! Тестовые данные созданы." 